# Pago cuota de socio

## Hola {{ $notifiable->nombre }}

como cada año por estas mismas fechas se abre el periodo para que nuestros socios y socias abonen su cuota anual.

Te recordamos, {{ $notifiable->nombre }}, que dicha cuota es por familia, es decir, es independiente del número de hijos/as que sean alumno/as del colegio.

Agradecemos que sigas formado parte de esta asociación cuyo único fin es hacer colegio y participar de forma, más activa si cabe, en la educación de nuestros hijos e hijas.

Por otro lado comentarte que recibes este correo porque no tienes aún domiciliado el pago de tu cuota, lo que te aconsejamos ya que pensamos que es más cómodo tanto para ti como para nuestra gestión. En ese caso dejarías de recibir este correo y no necesitarías justificarnos el pago de tu cuota ya que nosotros podemos confirmarlo si ninguna molestia para ti.

Mientras llega ese momento, si así lo estimas, te pedimos que una vez que hayas satisfecho el pago de tu cuota nos hagas llegar el justificante correspondiente por alguno de los medios siguientes:

* Presencialmente en nuestro despacho en el colegio donde procederemos a escanearlo para poder incorporarlo a nuestros archivos informáticos.
* Accediendo a nuestra web con las credenciales que te comunicamos en el momento de tu adhesión a esta AMPA (preferido).
* Adjuntándolo en un correo electrónico a la dirección: ampa@sanpedroapostol.es.

Sin más, agradecerte de nuevo tu colaboración.

¡¡ Gracias por usar nuestra aplicación !!

Un cordial saludo.

